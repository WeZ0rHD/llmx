/**
 * Claude Code adapter — renders `CLAUDE.md` from the current LLMX context.
 *
 * Claude Code reads `CLAUDE.md` (and `AGENTS.md` in newer versions). The
 * export is fully regenerable: re-running `llmx export claude` after a
 * session keeps the file aligned with the canonical state stored in
 * `project.llmx/`.
 */
import type { Repository } from '../../core/index.js';

export interface ExportResult {
  body: string;
  written: boolean; // false when nothing changed
}

export async function exportForClaudeCode(repo: Repository): Promise<string> {
  const [state, tasks, decisions, history, skills] = await Promise.all([
    repo.readProjectState(),
    repo.readTasks(),
    repo.readDecisions(),
    repo.readSessionHistory(),
    repo.readSkillIndex(),
  ]);

  const openTasks = tasks.tasks.filter((t) => t.status === 'pending' || t.status === 'in_progress');
  const lastSession = history[history.length - 1];

  const lines: string[] = [];
  lines.push('# Claude Code Instructions');
  lines.push('');
  lines.push('This project uses **LLMX** for shared AI agent context. The canonical');
  lines.push('state lives in `project.llmx/`. This file is auto-generated by');
  lines.push('`llmx export claude` — edit the source files in `project.llmx/` instead.');
  lines.push('');
  lines.push('## Always read before working');
  lines.push('');
  lines.push('- `project.llmx/memory/project-state.md`');
  lines.push('- `project.llmx/memory/tasks.json`');
  lines.push('- `project.llmx/memory/decisions.jsonl`');
  lines.push('- `project.llmx/sessions/latest.md`');
  lines.push('- `project.llmx/skills/index.json`');
  lines.push('');
  lines.push('## Project snapshot');
  lines.push('');
  lines.push(state.trim());
  lines.push('');
  lines.push('## Open tasks');
  lines.push('');
  if (openTasks.length === 0) {
    lines.push('_No open tasks._');
  } else {
    for (const t of openTasks) {
      const tags = t.tags.length > 0 ? `  [${t.tags.join(', ')}]` : '';
      lines.push(`- [${t.status}] **${t.title}**${tags}  _(${t.id})_`);
    }
  }
  lines.push('');
  lines.push('## Recent decisions');
  lines.push('');
  if (decisions.length === 0) {
    lines.push('_No decisions recorded yet._');
  } else {
    for (const d of decisions.slice(-5).reverse()) {
      const by = d.decidedBy ? ` _by ${d.decidedBy}_` : '';
      lines.push(`- **${d.title}**${by}  _(${d.createdAt})_`);
      if (d.rationale) {
        lines.push(`  - ${d.rationale}`);
      }
    }
  }
  lines.push('');
  lines.push('## Latest session');
  lines.push('');
  if (lastSession) {
    lines.push(`_Last session: ${lastSession.agent} on ${lastSession.createdAt}._`);
    lines.push('');
    lines.push(lastSession.summary);
  } else {
    lines.push('_No session summary yet._');
  }
  lines.push('');
  lines.push('## Available skills');
  lines.push('');
  if (skills.skills.length === 0) {
    lines.push('_No skills installed yet._');
  } else {
    for (const s of skills.skills) {
      lines.push(`- **${s.name}** — ${s.description || '(no description)'}  _(${s.path})_`);
    }
  }
  lines.push('');
  lines.push('## After work');
  lines.push('');
  lines.push('- Update `project.llmx/sessions/latest.md` with what you did and why.');
  lines.push('- Add important decisions to `project.llmx/memory/decisions.jsonl`.');
  lines.push('- Update `project.llmx/memory/tasks.json` (move tasks to `done` / `in_progress`).');
  lines.push('- Keep summaries concise and useful — short bullets beat long prose.');
  lines.push('');
  lines.push('## Rules');
  lines.push('');
  lines.push('- Never read or echo `.env` files or other secret material into shared context.');
  lines.push('- Prefer adding a decision entry over free-form prose for important choices.');
  lines.push('');
  lines.push('---');
  lines.push('');
  lines.push('_Generated by `llmx export claude`._');

  const body = lines.join('\n');
  await repo.writeText(repo.paths.claudeFile, body);
  return body;
}
